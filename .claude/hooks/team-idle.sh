#!/bin/bash
# ============================================================
# FPOF 에이전트 팀 — 팀원 유휴 품질 게이트
# TeammateIdle 훅: 팀원이 모든 작업 완료 후 유휴 상태 진입 시 발동
# 전체 작업 요약 보고서 작성을 지시
# ============================================================

PROJECT_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
STATE_FILE="$PROJECT_ROOT/.fpof-state.json"

# 현재 PDCA 단계 읽기
CURRENT_PHASE="unknown"
if [ -f "$STATE_FILE" ]; then
  CURRENT_PHASE=$(grep -o '"current_phase"[[:space:]]*:[[:space:]]*"[^"]*"' "$STATE_FILE" | head -1 | sed 's/.*"current_phase"[[:space:]]*:[[:space:]]*"\([^"]*\)"/\1/')
fi

cat <<EOF
[FPOF 팀원 유휴 전환] 작업 완료 정리

현재 PDCA 단계: $CURRENT_PHASE

모든 태스크가 완료되었습니다. 유휴 상태로 전환하기 전에 아래를 수행하세요:

## 완료 보고서 작성
다음 내용을 포함한 간략한 완료 보고서를 리드에게 제출하세요:
1. **완료된 태스크 목록**: 각 태스크별 산출물 파일 경로
2. **주요 의사결정**: 작업 중 내린 판단과 그 근거
3. **품질 자가 평가**: 각 산출물의 완성도 (상/중/하)
4. **발견된 이슈**: 다른 팀원이 알아야 할 사항
5. **다음 단계 제안**: 후속 작업이 필요한 경우

## 정리
- .fpof-state.json이 최신 상태인지 확인
- 임시 파일이 남아있으면 정리
- 리드에게 보고서 제출 후 종료 요청

보고서 제출 후 리드가 확인하면 세션이 종료됩니다.
EOF
